import { SermonRequest } from "../types";

function buildPrompt(request: SermonRequest): string {
  return `
أنت مساعد متخصص في كتابة خطب الجمعة والمحاضرات الشرعية وفق منهج أهل السنة والجماعة، بلغة عربية فصيحة، مؤثرة، منضبطة، بعيدة عن الغلو والتكلف.

المطلوب منك الآن: كتابة نص خطبة جاهز للإلقاء مباشرة، وليس خطة ولا مقالة ولا دراسة ولا ملخصًا.

بيانات الخطبة:
نوع المحتوى: ${request.type || "خطبة جمعة"}
موضوع الخطبة: ${request.topic || "موضوع عام"}
الجمهور المستهدف: ${request.audience || "عام"}
مدة الإلقاء: ${request.duration || "15 دقيقة"}
الأسلوب: ${request.style || "وعظي مؤثر"}
مستوى اللغة: ${request.languageLevel || "متوسطة"}
المناسبة: ${request.occasion || "بدون مناسبة"}
العناصر المطلوبة: ${JSON.stringify(request)}

تعليمات مهمة جدًا:
1. ابدأ مباشرة بمقدمة الخطبة الشرعية، ولا تكتب قبلها: فكرة الخطبة، الهدف، المحاور، تمهيد علمي، تعريفات، خطة، أو نقاط تحليلية.
2. لا تكتب عناوين كثيرة في بداية النص. المطلوب نص خطابي جاهز يسمعه الناس.
3. لا تجعل الخطبة مثل مقال معلوماتي أو بحث أكاديمي.
4. اجعلها خطبة جمعة مأثورة النسق: حمد وثناء، شهادتان، وصية بالتقوى، خطبة أولى، خاتمة الخطبة الأولى، جلسة، خطبة ثانية، دعاء.
5. لا تخترع آيات أو أحاديث أو مصادر.
6. استشهد بالقرآن الكريم استشهادًا مناسبًا للموضوع.
7. استشهد بالسنة النبوية الصحيحة أو الحسنة فقط، وإن لم تتحقق من درجة حديث فاكتب: "يحتاج إلى تحقق من درجته قبل استخدامه".
8. استأنس بأقوال الصحابة والسلف وأهل العلم عند الحاجة، ولا تنسب قولًا محددًا إلا إذا كان مشهورًا أو موثقًا.
9. يمكن إدراج بيت أو بيتين من الشعر العربي المعروف إن كان مناسبًا، بشرط ألا يكون الشعر مؤلفًا بالذكاء الاصطناعي.
10. لا تؤلف أبياتًا شعرية جديدة.
11. اربط الأدلة بالمعنى، ولا تسردها سردًا منفصلًا.
12. اجعل الخطبة مؤثرة، مترابطة، عملية، ومناسبة للجمهور.
13. حسّن الخاتمة بما يناسب الموضوع.
14. اجعل الدعاء من الأدعية المأثورة أو الجوامع المشهورة قدر الإمكان، مع دعاء مناسب للموضوع وحال المسلمين.
15. لا تضع في أول الخطبة تعريفًا أكاديميًا مطولًا، بل أدخل الموضوع من باب شرعي إيماني مناسب.

صيغة الإخراج المطلوبة حرفيًا:

# عنوان الخطبة

الحمد لله نحمده ونستعينه ونستغفره، ونعوذ بالله من شرور أنفسنا ومن سيئات أعمالنا، من يهده الله فلا مضل له، ومن يضلل فلا هادي له، وأشهد أن لا إله إلا الله وحده لا شريك له، وأشهد أن محمدًا عبده ورسوله.

أما بعد، فاتقوا الله عباد الله، واعلموا أن أصدق الحديث كتاب الله، وخير الهدي هدي محمد صلى الله عليه وسلم...

ثم ادخل مباشرة في موضوع الخطبة بأسلوب خطابي مؤثر.

## الخطبة الأولى

اكتب الخطبة الأولى كاملة، متضمنة:
- تقرير الأصل الشرعي للموضوع.
- آيات مناسبة.
- حديثًا صحيحًا أو حسنًا إن وجد ما يناسب.
- معنى تربوي وإيماني واضح.
- أمثلة واقعية منضبطة.
- توجيه عملي للمصلين.

ثم اختم الخطبة الأولى بعبارة مثل:
أقول قولي هذا، وأستغفر الله العظيم لي ولكم ولسائر المسلمين من كل ذنب، فاستغفروه، إنه هو الغفور الرحيم.

## جلسة الاستراحة

اكتب:
الحمد لله رب العالمين...

## الخطبة الثانية

ابدأ بالحمد والثناء والصلاة على النبي صلى الله عليه وسلم، ثم أكمل المعنى العملي للموضوع، واذكر الوصية بالتقوى، وركز على خلاصة جامعة وتطبيقات عملية.

## الخاتمة

اكتب خاتمة مؤثرة مناسبة للموضوع، قصيرة جامعة، تنقل المستمع إلى العمل.

## الدعاء

اكتب دعاءً مناسبًا من جوامع الدعاء، يتضمن:
- الصلاة على النبي صلى الله عليه وسلم.
- سؤال الهداية والتقوى والعفاف والغنى.
- صلاح القلوب والأعمال.
- المغفرة والرحمة.
- صلاح الأبناء والبيوت.
- أمن البلاد والعباد.
- نصرة المستضعفين من المسلمين إن كان مناسبًا.
- دعاء مناسب لموضوع الخطبة.

## ملاحظات للمراجعة قبل الإلقاء

اكتب في آخر النص فقط، وباختصار:
- راجع تخريج الأحاديث.
- راجع عزو الأقوال والأبيات إن وجدت.
- عدّل طول الخطبة بحسب وقت المسجد.

ممنوع في الإخراج:
- ممنوع كتابة "فكرة الخطبة العامة".
- ممنوع كتابة "الهدف من الخطبة".
- ممنوع كتابة "المحاور الرئيسية" في بداية الخطبة.
- ممنوع كتابة "تمهيد علمي مختصر".
- ممنوع إخراج النص كخطة أو نقاط بحثية.
- ممنوع البدء بتعريف أكاديمي للموضوع قبل مقدمة الخطبة.
`;
}

async function callGeminiFunction(prompt: string): Promise<string> {
  const response = await fetch("/.netlify/functions/gemini", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      prompt,
    }),
  });

  const data = await response.json().catch(() => ({}));

  if (!response.ok) {
    throw new Error(
      data?.details ||
      data?.error ||
      "فشل الاتصال بخدمة الذكاء الاصطناعي."
    );
  }

  return data.text || data.result || data.content || "";
}

export async function generateSermon(request: SermonRequest): Promise<string> {
  const prompt = buildPrompt(request);
  return await callGeminiFunction(prompt);
}

export async function editSermonAction(
  content: string,
  action: string
): Promise<string> {
  const prompt = `
أنت مساعد متخصص في تحسين الخطب والمحاضرات الشرعية.

لديك النص التالي:

${content}

المطلوب تنفيذه على النص:
${action}

التزم بما يلي:
1. حافظ على المعنى الأصلي.
2. لا تخترع آيات أو أحاديث أو مصادر.
3. لا تنسب أقوالًا إلى العلماء بلا تحقق.
4. حسّن النص بلغة عربية فصيحة ومؤثرة.
5. أعد النص كاملًا بعد التحسين، وليس مجرد ملاحظات.
6. إذا أضفت حديثًا أو أثرًا يحتاج تحققًا، فاكتب بجانبه: يحتاج إلى تحقق من درجته قبل استخدامه.
`;

  return await callGeminiFunction(prompt);
}
