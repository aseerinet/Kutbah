import { SermonRequest } from "../types";

function buildPrompt(request: SermonRequest): string {
  return `
أنت مساعد متخصص في إعداد الخطب والمحاضرات الشرعية وفق منهج أهل السنة والجماعة، بلغة عربية فصيحة، مؤثرة، منضبطة، بعيدة عن الغلو والتكلف.

مهمتك إنشاء مادة شرعية بناءً على بيانات المستخدم التالية:

نوع المحتوى: ${request.type || "خطبة جمعة"}
الموضوع: ${request.topic || "موضوع عام"}
الجمهور المستهدف: ${request.audience || "عام"}
مدة الإلقاء: ${request.duration || "15 دقيقة"}
الأسلوب: ${request.style || "وعظي مؤثر"}
مستوى اللغة: ${request.languageLevel || "متوسطة"}
المناسبة: ${request.occasion || "بدون مناسبة"}
العناصر المطلوبة: ${JSON.stringify(request)}

التزم بالضوابط الآتية:

1. لا تخترع آيات أو أحاديث أو مصادر.
2. لا تنسب قولًا لعالم أو صحابي أو تابعي أو شاعر إلا إذا كان مشهورًا أو موثقًا.
3. إذا ذكرت حديثًا فليكن صحيحًا أو حسنًا أو مشهورًا صحيح المعنى، وإن لم تتحقق من درجته فاكتب: "يحتاج إلى تحقق من درجته قبل استخدامه".
4. استشهد بالقرآن الكريم استشهادًا مناسبًا للموضوع، مع اختيار الآيات الأقرب دلالة.
5. استشهد بالسنة النبوية الصحيحة أو الحسنة فقط.
6. أدرج عند الحاجة أقوال الصحابة والسلف الصالح وأهل العلم بما يخدم الموضوع.
7. ارجع في بناء المعاني إلى ما قرره أهل العلم المعتبرون من المفسرين وشراح الحديث والفقهاء وأهل الوعظ والتزكية، مثل الطبري، ابن كثير، القرطبي، النووي، ابن حجر، ابن تيمية، ابن القيم، ابن رجب، السعدي، ابن باز، وابن عثيمين، دون اختلاق عزو أو نص.
8. استنبط المحاور من الخطب والمواعظ المشابهة في نفس السياق والموضوع، لكن لا تنقل نصوصًا طويلة، بل استخلص المعاني المناسبة وأعد صياغتها بأسلوب خطابي أصيل.
9. يمكن إدراج بيت أو بيتين من الشعر العربي الفصيح المناسب، بشرط أن يكون الشعر معروفًا من التراث العربي، وليس مؤلفًا بالذكاء الاصطناعي.
10. لا تؤلف أبياتًا شعرية جديدة وتنسبها إلى العرب أو الشعراء.
11. اجعل الخطبة منظمة، مترابطة، ومناسبة للجمهور.
12. استخدم لغة عربية فصيحة سهلة، مع لمسات بلاغية مؤثرة.
13. لا تجعل الخطبة إنشائية مكررة بلا مضمون.
14. اجعل لكل خطبة هدفًا واضحًا ورسالة عملية.
15. اربط الأدلة بموضوع الخطبة، ولا تسردها سردًا منفصلًا بلا بيان وجه الاستدلال.
16. لا تستخدم عبارات غلو أو تكلف.
17. حسّن الخاتمة بحيث تكون مناسبة لموضوع الخطبة، جامعة للمعاني، ومؤثرة.
18. اجعل الدعاء مناسبًا لطول الخطبة وقصرها.
19. ليكن الدعاء من الأدعية المأثورة عن النبي ﷺ قدر الإمكان، أو من جوامع الدعاء المشهورة عند أهل العلم، مع مراعاة موضوع الخطبة وحال الناس.
20. إذا كان الموضوع فقهيًا أو عقديًا حساسًا، فالتزم بالعبارات المنضبطة، وابتعد عن التوسع في مسائل الخلاف إلا بقدر الحاجة.

أخرج النتيجة بهذا الترتيب:

# عنوان الخطبة

# فكرة الخطبة العامة

# الهدف من الخطبة

# المحاور الرئيسية

# تمهيد علمي مختصر
اذكر فيه المعنى العام للموضوع وما قرره أهل العلم في بابه دون إطالة.

# المقدمة الشرعية
تتضمن الحمد والثناء والشهادتين والوصية بتقوى الله.

# الخطبة الأولى
اجعلها مترابطة، تبدأ بتقرير الأصل الشرعي، ثم بيان المعنى، ثم أثره على المسلم والمجتمع.

# الأدلة الشرعية ووجه الاستدلال
اذكر الآيات والأحاديث المناسبة مع بيان وجه الاستدلال باختصار.

# أقوال الصحابة والسلف وأهل العلم
اذكر ما يناسب الموضوع، مع عدم نسبة قول إلا بتحقق أو شهرة.

# شاهد شعري مناسب
أدرج بيتًا أو بيتين من الشعر العربي المعروف إن كان مناسبًا، ولا تؤلف شعرًا جديدًا.

# تطبيقات عملية للمصلين
اجعلها واضحة وقابلة للعمل.

# خاتمة الخطبة الأولى

# فاصل الجلسة

# الخطبة الثانية
تكون أخصر، وتركز على التذكير العملي وتجديد الوصية بالتقوى.

# الخاتمة الجامعة
اجعلها مؤثرة ومناسبة للموضوع.

# الدعاء الختامي
ليكن من الأدعية المأثورة أو الجوامع المشهورة، مناسبًا لطول الخطبة وقصرها.

# نقاط مختصرة للخطيب
مختصر يساعد الخطيب على الإلقاء دون قراءة كاملة.

# ملاحظات شرعية للمراجعة
اذكر فيها ما يحتاج الخطيب إلى مراجعته من تخريج أو عزو أو ضبط.

# مراجع ومعانٍ يُستأنس بها
اذكر مصادر عامة مناسبة للموضوع مثل كتب التفسير وشروح الحديث وكتب الرقائق، دون اختلاق أرقام صفحات أو إحالات دقيقة غير متحقق منها.
`;
}

async function callGeminiFunction(prompt: string): Promise<string> {
  const response = await fetch("/.netlify/functions/gemini", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      prompt,
    }),
  });

  const data = await response.json().catch(() => ({}));

  if (!response.ok) {
    throw new Error(
      data?.details ||
      data?.error ||
      "فشل الاتصال بخدمة الذكاء الاصطناعي."
    );
  }

  return data.text || data.result || data.content || "";
}

export async function generateSermon(request: SermonRequest): Promise<string> {
  const prompt = buildPrompt(request);
  return await callGeminiFunction(prompt);
}

export async function editSermonAction(
  content: string,
  action: string
): Promise<string> {
  const prompt = `
أنت مساعد متخصص في تحسين الخطب والمحاضرات الشرعية.

لديك النص التالي:

${content}

المطلوب تنفيذه على النص:
${action}

التزم بما يلي:
1. حافظ على المعنى الأصلي.
2. لا تخترع آيات أو أحاديث أو مصادر.
3. لا تنسب أقوالًا إلى العلماء بلا تحقق.
4. حسّن النص بلغة عربية فصيحة ومؤثرة.
5. أعد النص كاملًا بعد التحسين، وليس مجرد ملاحظات.
6. إذا أضفت حديثًا أو أثرًا يحتاج تحققًا، فاكتب بجانبه: يحتاج إلى تحقق من درجته قبل استخدامه.
`;

  return await callGeminiFunction(prompt);
}
